#For me permite correr letra por letra una cadena de caracteres.

# Definir una lista de frutas
lista_frutas = ["Manzana", "Banana", "Cereza", "Maracuyá"]

# Utilizar un ciclo for para imprimir cada fruta en la lista
for fruta in lista_frutas:
    print(fruta)

for indice in range (4): #indice = posición dentro de la lista y range: cantidad de elementos en la lista
    print(lista_frutas[indice])