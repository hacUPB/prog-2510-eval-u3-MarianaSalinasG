# Agregar Elementos a una Lista

PilotosF1 = ["Lando Norris", "Lewis Hamilton", "Sebastian Vettel"]

# Agregar un elemento al final de la lista
PilotosF1.append("Daniel Ricciardo")

# Insertar un elemento en una posición específica
PilotosF1.insert(1, "Alex Albon")

# Extender la lista con otra lista
PilotosF1.extend(["Charles Leclerc", "George Russell"])

print(PilotosF1)